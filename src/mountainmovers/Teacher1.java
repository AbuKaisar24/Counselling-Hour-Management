   public ArrayList<Mountainmovers> ListUsers(String Name)
    {
        ArrayList<Mountainmovers> usersList = new ArrayList<Mountainmovers>();
        
     
        
        try{
 
            String sql = "select name,day,hour ,mobile  from coun order by  name asc";
                
           pst=conn.prepareStatement(sql);
                 rs=pst.executeQuery();
            
            
           Mountainmovers m;
            
            while(rs.next())
            {
                m = new Mountainmovers(
                                 rs.getString("name"),
                                 rs.getString("day"),
                                 rs.getString("hour"),
                                 rs.getString("mobile")
                                );
                usersList.add(m);
            }
            
        }catch(Exception ex){
            System.out.println(ex.getMessage());
        }
        
        return usersList;
    }
    
    
     public void findUsers()
    {
        ArrayList<Mountainmovers> m= ListUsers(jTextField9.getText());
        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{"Name","Day","Hour","Mobile"});
        Object[] row = new Object[4];
        
        for(int i = 0; i < m.size(); i++)
        {
            row[0] = m.get(i).getName();
            row[1] = m.get(i).getDay();
            row[2] = m.get(i).getHour();
            row[3] = m.get(i).getMobile();
            model.addRow(row);
        }
       jTable1.setModel(model);
    }
